function mass(value, unit = "lb") {
  return { value, unit };
}

function r(bodyFatPercentage, totalMass, fatMass, leanMass, boneMineralContent) {
  return {
    bodyFatPercentage,
    totalMass: mass(totalMass),
    fatMass: mass(fatMass),
    leanMass: mass(leanMass),
    boneMineralContent: mass(boneMineralContent),
  };
}

function createBodySpecDEXAScan({
  date,
  summary,
  rmr,
  vatMass,
  vatVolume,
  androidFatPercentage,
  gynoidFatPercentage,
  androidGynoidRatio,
  regionalAssessment,
  muscleBalance,
  boneDensity,
}) {
  const sourceFileId = `${date}-bodyspec-dexa.pdf`;

  return createDEXAScan({
    id: `dexa_${date.replaceAll("-", "_")}`,
    userId: USER_ID,
    measuredAt: date,
    date,
    relatedGoalIds: [
      BODY_FAT_GOAL_ID,
      LEAN_MASS_GOAL_ID,
      VISIBLE_ABS_GOAL_ID,
    ],
    provider: "BodySpec",
    totalMass: summary.totalMass,
    bodyFatPercentage: summary.bodyFatPercentage,
    fatMass: summary.fatMass,
    leanMass: summary.leanMass,
    boneMass: summary.boneMineralContent,
    boneMineralContent: summary.boneMineralContent,
    restingMetabolicRate: {
      value: rmr,
      unit: "kcal/day",
    },
    visceralFat: null,
    visceralAdiposeTissue: {
      mass: mass(vatMass),
      volume: mass(vatVolume, "in3"),
    },
    androidFatPercentage,
    gynoidFatPercentage,
    androidGynoidRatio,
    regionalAssessment,
    muscleBalance,
    boneDensity: {
      totalBMD: boneDensity.totalBMD,
      tScore: null,
      zScore: boneDensity.ageMatchedZScore,
      youngAdultZScore: boneDensity.youngAdultZScore,
      ageMatchedZScore: boneDensity.ageMatchedZScore,
    },
    sourceFileId,
    source: {
      type: "dexa",
      name: "BodySpec",
      externalId: null,
      importedAt: null,
      confidence: "high",
      notes: "Verified BodySpec DEXA report.",
    },
    fieldProvenance: {
      imported: importedFields,
      computed: [],
    },
    createdAt: null,
    updatedAt: null,
  });
}

